name = "Amrit0123456789"

print(name[0:2]) #goes from 0 to 2-1 ie 1

print(name[0:-1]) # same as name[0:4]

# print(name[0:10:n]) # skips n-1 characters
print(name[0:10:1]) # skip 0 characters
print(name[0:10:2]) # skip 1 characters
print(name[0:10:3]) # skip 2 characters

print(name[:4]) # default will be 0
print(name[1:]) # default will be n-1 = 14